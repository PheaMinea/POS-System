    function confirmPayment() {
        isConfirming = true;
        confirmBtn.disabled = true;
        confirmBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Confirming...';
        confirmStatus.className = 'mt-4 text-sm text-slate-500 text-center';
        confirmStatus.textContent = 'Confirming your payment...';

        fetch('{{ route('customer.payment.confirm-cash', $order->id) }}', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'X-CSRF-TOKEN': '{{ csrf_token() }}',
                'Accept': 'application/json',
            },
        })
        .then(response => response.json())
        .then(data => {
            if (data.success && data.is_paid) {
                confirmStatus.className = 'mt-4 text-sm text-emerald-600 text-center font-medium';
                confirmStatus.innerHTML = '<i class="fas fa-check-circle mr-2"></i> ' + (data.message || 'Payment confirmed!');

                Swal.fire({
                    icon: 'success',
                    title: 'Payment Confirmed!',
                    text: data.message || 'Your order is now pending restaurant acceptance.',
                    confirmButtonColor: '#2563eb',
                    confirmButtonText: 'View Order',
                }).then(() => {
                    window.location.href = '{{ route("customer.orders.show", $order->id) }}';
                });
            } else {
                confirmStatus.className = 'mt-4 text-sm text-rose-600 text-center';
                confirmStatus.innerHTML = '<i class="fas fa-exclamation-circle mr-2"></i> ' + (data.message || 'Something went wrong');
                isConfirming = false;
                confirmBtn.disabled = false;
                confirmBtn.innerHTML = '<i class="fas fa-check-circle"></i> I Have Paid - Confirm Payment';
            }
        })
        .catch(error => {
            confirmStatus.className = 'mt-4 text-sm text-rose-600 text-center';
            confirmStatus.innerHTML = '<i class="fas fa-exclamation-circle mr-2"></i> Unable to confirm payment right now';
            isConfirming = false;
            confirmBtn.disabled = false;
            confirmBtn.innerHTML = '<i class="fas fa-check-circle"></i> I Have Paid - Confirm Payment';
            console.error(error);
        });
    }

    console.log('💰 Cash Payment page loaded');
</script>
