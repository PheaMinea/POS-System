<script>
    window.customerPaymentQr = {
        data: @json($qr['qr_data'] ?? ''),
        image: @json($qr['qr_image'] ?? ''),
    };

    // ===== Generate KHQR using JavaScript fallback =====
    const BAKONG_MERCHANT_ID = '{{ $merchantAccountId }}';
    const MERCHANT_NAME = '{{ $merchantName }}';
    const MERCHANT_CITY = '{{ $merchantCity }}';
    const ORDER_TOTAL = {{ $order->total_price }};
    const ORDER_ID = {{ $order->id }};

    function generateKHQR() {
        // Bakong KHQR format (simplified merchant individual QR)
        // Format: merchant_account_id|merchant_name|merchant_city|amount|currency|store_label|terminal_label
        const khqrData = [
            BAKONG_MERCHANT_ID,
            MERCHANT_NAME,
            MERCHANT_CITY,
            ORDER_TOTAL.toFixed(2),
            'USD',
            'FoodRestaurant',
            'POS'
        ].join('|');

        return khqrData;
    }

    function renderQRCode() {
        const qrContainer = document.getElementById('qrcode');
        qrContainer.innerHTML = '';

        if (window.customerPaymentQr.image) {
            qrContainer.innerHTML = `<img src="${window.customerPaymentQr.image}" alt="Bakong KHQR" class="w-64 h-64 object-contain">`;
            return;
        }

        const khqrString = window.customerPaymentQr.data || generateKHQR();

        try {
            // Generate QR code using qrcodejs library
            const qr = new QRCode(qrContainer, {
                text: khqrString,
                width: 260,
                height: 260,
                colorDark: '#1e293b',
                colorLight: '#ffffff',
                correctLevel: QRCode.CorrectLevel.H
            });

            console.log('✅ KHQR generated successfully via JavaScript');
            console.log('📱 QR Data:', khqrString);
        } catch (e) {
            console.error('❌ QR Generation failed:', e);
            qrContainer.innerHTML = `
                <div class="text-slate-400 text-center p-4">
                    <i class="fas fa-exclamation-triangle text-3xl block mb-2"></i>
                    <p class="text-sm">Could not generate QR code</p>
                    <p class="text-xs mt-1 font-mono break-all">${khqrString}</p>
                </div>
            `;
        }
    }

    // Render QR on page load
    document.addEventListener('DOMContentLoaded', function() {
        renderQRCode();
    });

    // ===== Payment Verification =====
    const verifyBtn = document.getElementById('verifyPaymentBtn');
    const statusBox = document.getElementById('paymentStatus');
    const autoHint = document.getElementById('autoPaymentHint');
    let isChecking = false;
    let checkCount = 0;
    let isPaid = false;

    function verifyPayment() {
        if (isChecking || isPaid) return;

        isChecking = true;
        checkCount++;
        verifyBtn.disabled = true;
        verifyBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Checking...';
        statusBox.className = 'mt-4 text-sm text-slate-500 text-center';
        statusBox.textContent = 'Checking payment status... (Attempt ' + checkCount + ')';

        fetch('{{ route('customer.payment.verify', $order->id) }}', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'X-CSRF-TOKEN': '{{ csrf_token() }}',
                'Accept': 'application/json',
            },
        })
        .then(response => response.json())
        .then(data => {
            if (data.success && data.is_paid) {
                isPaid = true;
                statusBox.className = 'mt-4 text-sm text-emerald-600 text-center font-medium';
                statusBox.innerHTML = '<i class="fas fa-check-circle mr-2"></i> ' + (data.message || 'Payment verified!');
                autoHint.textContent = '✅ Payment confirmed!';

                // Auto-redirect to order page after 2 seconds
                setTimeout(() => {
                    window.location.href = '{{ route("customer.orders.show", $order->id) }}';
                }, 2000);

                return;
            }

            statusBox.className = 'mt-4 text-sm text-amber-600 text-center';
            statusBox.innerHTML = '<i class="fas fa-clock mr-2"></i> ' + (data.message || 'Payment is still pending');
            autoHint.textContent = '🔄 Auto-checking payment every 10 seconds (Attempt ' + checkCount + ')';
        })
        .catch(error => {
            statusBox.className = 'mt-4 text-sm text-rose-600 text-center';
            statusBox.innerHTML = '<i class="fas fa-exclamation-circle mr-2"></i> Unable to verify payment right now';
            autoHint.textContent = '⚠️ Retrying in 10 seconds';
            console.error(error);
        })
        .finally(() => {
            isChecking = false;
            verifyBtn.disabled = false;
            verifyBtn.innerHTML = '<i class="fas fa-rotate-right mr-2"></i> Check Payment Status';
        });
    }

    // Event listener
    verifyBtn.addEventListener('click', verifyPayment);

    // Auto-check every 10 seconds
    setTimeout(() => {
        verifyPayment();
    }, 2000);
    const interval = setInterval(verifyPayment, 10000);

    // Cleanup on page unload
    window.addEventListener('beforeunload', function() {
        clearInterval(interval);
    });

    // Check if already paid on page load (redirect with SweetAlert2)
